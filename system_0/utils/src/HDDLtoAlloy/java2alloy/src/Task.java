

public class Task {
   public String name;
   public String capability;
   
   public Task(String n, String capability) {
	   this.name = n;
	   this.capability = capability;
   }
   
   public String getName() {
	   return this.name;
   }
   
   public String getCapab() {
	   return this.capability;
   }
   
}
